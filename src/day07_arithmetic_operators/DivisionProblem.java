package day07_arithmetic_operators;

public class DivisionProblem {
    public static void main(String[] args){
        System.out.println(10/3); // 3
        System.out.println(5/2);  // 2
        int num1 = 40;
        int num2 = 15;
        System.out.println(num1 / num2); // 2
        System.out.println(40.0/ 15.0);  // 2.6
        double d1 = 12;
        double d2 = 5;
        System.out.println(d1 / d2); // 2.4 because it's double

        int count = 100;
        double d_count = 30;
        System.out.println(count /  d_count);  // 3.33







    }
}
