package day48_constructors_static.StaticExamples;

public class Calculator {
    // static method
    public static void add(int num1, int num2) {
        System.out.println(num1 + num2);
    }
    // instance/
    public void multiply(int num1, int num2) {
        System.out.println(num1 * num2);
    }
}
