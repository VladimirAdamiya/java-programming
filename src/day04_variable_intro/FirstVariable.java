package day04_variable_intro;

public class FirstVariable {
    public static void main(String[] args){
        //declare variable n that can store int(whole number)
        int n;
        //assign value 10 to n
        n = 10;
        //print value of n variable
        System.out.println(n);
        System.out.println("n");
        int apples;
        apples = 50;
        System.out.println(apples);

        int vovan;
        vovan = 100;
        System.out.println(vovan);
        int bananas;
        bananas = 100;
        System.out.println(bananas);

    }

}
