package day05_primitives_concatanation;

public class PrimitiveExamples {
    public static void main(String[] args){
        // ****** WHOLE NUMBERS/INTEGER TYPES ******
        byte byteValue = 100;
        short shortValue = 3536;
        int intValue = 2123333384;
        long longValue = 3562266632266523636l;
        // ****** DECIMAL NUMBER/FLOATING POINT TYPES ******
        float floatValue = 2345.55f; //float variable value we put "f" in the end, otherwise system will think this is double
        double doubleValue = 32365998.55;  // "D" in the end is also valid
        //****** CHARACTER TYPE ******
        char charValue = 'a';   // char is a single character in single quotes ' '
        //****** BOOLEAN - TRUE/FALSE ******
        boolean booleanValue = true;
        boolean booleanValue1 = false;
        //****** PRINT VARIaBLE VALUES ******

        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(true);
        System.out.println(false);


    }

}
