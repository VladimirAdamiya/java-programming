package Replit;

public class Swapping2Numbers {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;
        int temp;
        System.out.println(num1 + ", " + num2);

        temp = num1; // temp = 5
        num1 = num2; // num1 = 10
        num2 = temp; // num2 = 5
        System.out.println(num1 + ", " + num2);

    }
}

